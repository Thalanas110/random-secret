let current = -1;

/* Lock timer logic */
function initializeLockTimer() {
    const unlockDate = new Date('2026-05-10T11:00:00+08:00'); // May 10, 2026 at 11:00am GMT+8
    const lockOverlay = document.getElementById('lockOverlay');
    
    function updateTimer() {
        const now = new Date();
        const timeDiff = unlockDate - now;
        
        if (timeDiff <= 0) {
            // Time has passed, hide the lock
            lockOverlay.classList.add('hidden');
            return;
        }
        
        // Calculate time units
        const days = Math.floor(timeDiff / (1000 * 60 * 60 * 24));
        const hours = Math.floor((timeDiff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((timeDiff % (1000 * 60 * 60)) / (1000 * 60));
        const seconds = Math.floor((timeDiff % (1000 * 60)) / 1000);
        
        // Update display
        document.getElementById('timerDays').textContent = String(days).padStart(2, '0');
        document.getElementById('timerHours').textContent = String(hours).padStart(2, '0');
        document.getElementById('timerMinutes').textContent = String(minutes).padStart(2, '0');
        document.getElementById('timerSeconds').textContent = String(seconds).padStart(2, '0');
    }
    
    // Initial update
    updateTimer();
    
    // Update every second
    setInterval(updateTimer, 1000);
}

// Initialize lock timer when page loads
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeLockTimer);
} else {
    initializeLockTimer();
}

/* Build grid */
const grid = document.getElementById('poemGrid');
POEMS.forEach((poem, i) => {
const card = document.createElement('article');
card.className = 'poem-card';
card.setAttribute('tabindex', '0');
card.setAttribute('role', 'button');
card.setAttribute('aria-label', 'Read: ' + poem.title);
card.innerHTML =
    '<p class="card-number">Poem ' + String(i + 1).padStart(2, '0') + '</p>' +
    '<h3 class="card-title">' + poem.title + '</h3>' +
    '<p class="card-preview">' + poem.preview + '</p>' +
    '<div class="card-footer">' +
    (poem.lang ? '<span class="card-lang">' + poem.lang + '</span>' : '<span></span>') +
    '<div class="card-arrow">&#8594;</div>' +
    '</div>';
card.addEventListener('click', () => open(i));
card.addEventListener('keydown', e => { if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); open(i); } });
grid.appendChild(card);
setTimeout(() => card.classList.add('appear'), 80 + i * 65);
});

/* Build nav dots */
const dotsEl = document.getElementById('navDots');
POEMS.forEach((_, i) => {
const d = document.createElement('button');
d.className = 'nav-dot';
d.setAttribute('aria-label', 'Poem ' + (i + 1));
d.addEventListener('click', () => open(i));
dotsEl.appendChild(d);
});

function open(i) {
current = i;
render();
document.getElementById('viewerOverlay').classList.add('open');
document.body.style.overflow = 'hidden';
document.getElementById('viewerClose').focus();
}

function close() {
document.getElementById('viewerOverlay').classList.remove('open');
document.body.style.overflow = '';
}

function render() {
const p = POEMS[current];
document.getElementById('viewerNum').textContent = 'Poem ' + String(current + 1).padStart(2, '0') + ' of ' + POEMS.length;
document.getElementById('viewerTitle').textContent = p.title;

const stanzasEl = document.getElementById('viewerStanzas');
stanzasEl.innerHTML = '';
p.stanzas.forEach(stanza => {
    const sEl = document.createElement('div');
    sEl.className = 'poem-stanza';
    stanza.forEach(line => {
    const lEl = document.createElement('p');
    lEl.className = 'poem-line';
    lEl.textContent = line;
    sEl.appendChild(lEl);
    });
    stanzasEl.appendChild(sEl);
});
stanzasEl.parentElement.scrollTop = 0;

document.getElementById('navPrev').disabled = current === 0;
document.getElementById('navNext').disabled = current === POEMS.length - 1;
document.querySelectorAll('.nav-dot').forEach((d, i) => d.classList.toggle('active', i === current));
}

document.getElementById('viewerClose').addEventListener('click', close);
document.getElementById('viewerBackdrop').addEventListener('click', close);
document.getElementById('navPrev').addEventListener('click', () => { if (current > 0) open(current - 1); });
document.getElementById('navNext').addEventListener('click', () => { if (current < POEMS.length - 1) open(current + 1); });
document.addEventListener('keydown', e => {
if (!document.getElementById('viewerOverlay').classList.contains('open')) return;
if (e.key === 'Escape') close();
if (e.key === 'ArrowLeft' && current > 0) open(current - 1);
if (e.key === 'ArrowRight' && current < POEMS.length - 1) open(current + 1);
});

